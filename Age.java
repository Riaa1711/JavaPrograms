class  Age
{
	public static void main(String[] args) 
	{
		int age =24;
		if (age >=60)
		{
			System.out.println(" Senior");
		}
		else if (age <60 && age >=20)
		{
			System.out.println("Adult");
		}
		else if (age <20 && age >=13)

		{
			System.out.println(" Teenager");
		}
		else 
		{
			System.out.println(" Child");
		}

		
		
	}
}
